package draft.model.draftRepository.nodes.elements;

public class RoomElementInfo {
    private RoomElementType type;
    private int width;
    private int height;

    public RoomElementType getType() {
        return type;
    }

    public void setType(RoomElementType type) {
        this.type = type;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
